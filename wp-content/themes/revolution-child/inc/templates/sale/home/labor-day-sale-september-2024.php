    function handleMove(e) {
    let clientX, clientY;

    if (e.type === 'mousemove') {
        clientX = e.clientX;
        clientY = e.clientY;
    } else if (e.type === 'touchmove') {
        clientX = e.touches[0].clientX;
        clientY = e.touches[0].clientY;
    }

    const { innerWidth, innerHeight } = window;
    const moveX = (clientX / innerWidth - 0.4) * 20;
    const moveY = (clientY / innerHeight - 0.4) * 20;

    const laborSaleSection = document.querySelector('.laborSaleSection');
    if (!laborSaleSection) {
        console.error('Element with class laborSaleSection not found.');
        return;
    }

    const rect = laborSaleSection.getBoundingClientRect();
    if (
        clientX >= rect.left &&
        clientX <= rect.right &&
        clientY >= rect.top &&
        clientY <= rect.bottom
    ) {
        document.querySelectorAll('.animate').forEach(element => {
            element.style.transform = `translate(${moveX}px, ${moveY}px)`;
        });
    }
}

document.addEventListener('mousemove', handleMove);
document.addEventListener('touchmove', handleMove);







