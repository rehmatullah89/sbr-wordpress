        // Function for smooth scrolling
        function scrollToElement(element, duration, offset) {
            var start = window.pageYOffset;
            var target = element.offsetTop + offset;
            var distance = target - start;
            var startTime = null;

            function animateScroll(time) {
                if (!startTime) startTime = time;
                var progress = time - startTime;
                var easeInOutCubic = progress < duration ? progress / duration : 1;
                window.scrollTo(0, start + distance * easeInOutCubic);
                if (progress < duration) {
                    requestAnimationFrame(animateScroll);
                }
            }

            requestAnimationFrame(animateScroll);
        }

        // Loop through each button and add a click event listener
        scrollToButtons.forEach(function(button) {
            button.addEventListener('click', function() {
                // Get the target section ID from the "data-section" attribute
                var targetSection = this.getAttribute('data-section');

                // Get the target section element
                var targetElement = document.getElementById(targetSection);

                if (targetElement) {
                    // Scroll to the target section with custom smooth scrolling and offset
                    scrollToElement(targetElement, 300, -70); // 1000 milliseconds and -30px offset (adjust as needed)
                }
            });
        });




