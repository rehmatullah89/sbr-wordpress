    // Adjust the speed based on your preference
    var parallaxSpeed = 0.5;
    // Adjust the speed based on your preference
    $(window).scroll(function() {
        var scrollTop = $(this).scrollTop();
        $('.parallax-window').css('background-position', 'center ' + -(scrollTop * parallaxSpeed) +
            'px');
    });
    // Hide all answers initially
    $('.accordion-list > li > .answer').hide();

    // Automatically open the third accordion item
    var $thirdAccordionItem = $('.accordion-list > li:nth-child(3)');
    $thirdAccordionItem.addClass("active").find(".answer").slideDown();

    // Toggle accordion items on click
    $('.accordion-list > li').click(function(event) {
        // Prevent the event from bubbling up to parent elements
        event.stopPropagation();

        if ($(this).hasClass("active")) {
            $(this).removeClass("active").find(".answer").slideUp();
        } else {
            $(".accordion-list > li.active .answer").slideUp();
            $(".accordion-list > li.active").removeClass("active");
            $(this).addClass("active").find(".answer").slideDown();

            // Scroll to the top of the opened accordion item only on mobile devices
            if ($(window).width() <
                768) { // Assuming mobile devices have a viewport width less than 768px
                $('html, body').animate({
                    scrollTop: $(this).offset().top
                }, 500); // You can adjust the scroll speed if needed
            }
        }
        return false;
    });

    // Stop event propagation when clicking on .answer-content
    $('.accordion-list > li > .answer > .answer-content').click(function(event) {
        event.stopPropagation();
    });

});
